#include "pch.h"

#define CLT_VERSION 168 // Hahaha Client Version // steph
#define SUB_VERSION "V2" // steph
#define BUILD_NUMBER "Rebirth" //__DATE__ " " __TIME__ // steph



// Sub versions:
// a : alpha
// b : beta
// g : release

 
//////////////////////////////////////////////////////////////////////////////////////////
#include "version.h"
#include <stdio.h>

namespace Version{

char versionBuffer[ 256 ] = { 0 };

//////////////////////////////////////////////////////////////////////////////////////////
const char *GetVersionText( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the text version
//////////////////////////////////////////////////////////////////////////////////////////
{
    if( versionBuffer[ 0 ] == 0 ){
        sprintf( versionBuffer, "Version %.2f%s, %s", ((float)CLT_VERSION)/100, SUB_VERSION, BUILD_NUMBER );
    }

    return versionBuffer;
}
//////////////////////////////////////////////////////////////////////////////////////////
unsigned long GetVersion( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the number version
//////////////////////////////////////////////////////////////////////////////////////////
{
    return CLT_VERSION;
}
//////////////////////////////////////////////////////////////////////////////////////////
const char* GetBuild( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the build number.
//////////////////////////////////////////////////////////////////////////////////////////
{
    return BUILD_NUMBER;
}

//////////////////////////////////////////////////////////////////////////////////////////
char* GetSubVersion( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the sub version character.
//////////////////////////////////////////////////////////////////////////////////////////
{
    return SUB_VERSION;
}

};

